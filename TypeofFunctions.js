//Tipe - tipe Function

//Function Declaration
function sayHello() {
  console.log("Hello World!");
}

sayHello(); // Akan Print Hello World!

//Function Expression
let sayHello2 = function () {
  console.log("Hello World!");
};

sayHello2(); // Akan Print Hello World!

//Arrow Function
let sayHello3 = () => {
  console.log("Hello World!");
};

sayHello3(); // Akan Print Hello World!

//Arrow Function Dengan Return
let sayHello4 = () => {
  return "Hello World!";
};

console.log(sayHello4()); // Akan Print Hello World!

//Apa perbedaan antara Function Declaration, Function Expression, dan Arrow Function?
//Function Declaration adalah cara yang paling umum digunakan untuk membuat function.
//Function Expression adalah cara lain untuk membuat function.
//Arrow Function adalah cara lain untuk membuat function.

